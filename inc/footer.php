          <p>FRAT<sup>&#174;</sup> (Folate Receptor Antibody Test) is a diagnostic blood test that screens for Folate Receptor Autoantibodies.</p>
          <p>The presence of Folate Receptor Autoantibodies suggests that folate (vitamin B9), which is an essential vitamin for proper growth and neurological function, is not being properly transported to the brain and into the cerebrospinal fluid (CSF). </p>
          <p>Folate Receptor Autoantibodies have been found in Cerebral Folate Deficiency Syndrome, Autism Spectrum Disorders, Depression, Schizophrenia as well as other neuropsychiatric and neurodevelopmental disorders. 
</p>
<p>Diagnosis of Folate Receptor Autoantibodies should be done as early as possible as this may affect treatment outcomes. </p>
<p><b>Please consult your physician for further information. </b></p>
