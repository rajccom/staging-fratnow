                  </button>
                </h2>
                <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionExample">
                  <div class="accordion-body">
                  <p>FRAT<sup>®</sup> consists of 2 parts – a blocking assay and a binding assay. “Assay” is the technical laboratory term for a quantitative test of a substance. In other words, the FRAT<sup>®</sup> “assays” quantify the amount of blocking autoantibodies and binding autoantibodies present. Both types of autoantibodies inhibit the transport of Folate to tissues such as the brain. Blocking autoantibodies directly block the access of Folate to the receptor, while binding autoantibodies are thought to affect the general position of the receptor so that Folate can't get through it and into the tissue properly. Indirectly, the assays inform us about the availability of Folate (and its active form called 5-MTHF) in these tissues.</p>      </div>
