          <h3>Blood Sample Collection and Shipping</h3>
         
         <ul class="site-features-order">

                                <li>Acceptable samples are whole blood and serum.</li>
                                <li>Draw 2–4 ml of blood into a red-top tube or SST tube (serum). Please note that the FRAT<sup>®</sup> kit contains SST tubes.</li>
                                <li>If feasible: Remove red cells by centrifugation, transfer serum/plasma to clean tube and refrigerate (4°C) or freeze (-20°C) until time of shipping.</li>
                                <li>If whole blood must be sent, collect into red top tube and ship the same day. Do NOT freeze whole blood.</li>
                                <li>Ship stored serum/plasma with freezer packs. Ship whole blood at ambient temperature. If overnight shipment of serum/plasma is not possible or a lengthy transit time is anticipated, shipment with dry ice is recommended. Do NOT ship whole blood with dry ice.</li>
                                <li>Ship all tubes as PRIORITY OVERNIGHT using FedEx or UPS for delivery weekdays. No weekend or holiday deliveries will be accepted.</li>
                                <li>Minimum volume of serum or plasma acceptable is 1 ml (2 ml for whole blood).</li>
                                <li>Please make sure that each sample is properly and securely labeled, including name (or ID) and date.</li>
                                <li>Complete and include our <a href="pdf/FRAT-Lab-Requsition-Form.pdf"> FRAT<sup>®</sup> Laboratory Test Form</a> with your shipment</li>
